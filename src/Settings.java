public class Settings {

    private int bgColor;
    private int soundSettings;
    public Settings(){

        bgColor = 0;
        soundSettings = 0;

    }
    public int getBgColor(){

        return bgColor;

    }
    public void setBgColor(int x){

        bgColor = x;

    }
    public int getSoundSettings(){

        return soundSettings;

    }
    public void setSoundSettings(int x){

        soundSettings = x;

    }

}
